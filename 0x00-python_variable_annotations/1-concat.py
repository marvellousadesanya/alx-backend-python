#!/usr/bin/env python3
"""
This module defines a function that concatenates two strings.
"""

def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings and return the result.

    Args:
        str1: The first string to concatenate.
        str2: The second string to concatenate.

    Returns:
        The concatenation of str1 and str2 (as a string).
    """
    return str1 + str2

