#!/usr/bin/env python3
"""
This module defines a function that adds two floats.
"""

def add(a: float, b: float) -> float:
    """
    Add two floats and return the result.

    Args:
        a: The first float to add.
        b: The second float to add.

    Returns:
        The sum of a and b (as a float).
    """
    return a + b

